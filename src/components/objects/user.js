export class User {

	constructor(id, last_name, first_name, email, phone_num, company, role, loyalty_points) {
		this.id = id;
		this.last_name = last_name;
		this.first_name = first_name;
		this.email = email;
		this.phone_num = phone_num;
		this.company = company;
		this.role = role;
		this.loyalty_points = loyalty_points;
	}


}